const grade = "A+"

switch (grade) {
    case 'A+':
        console.log("good job");
        break;
    case 'A':
        console.log("pretty good");
        break;
    case 'B':
        console.log("not bad");
        break;
    case 'F':
        console.log("failed");
        break;
    default:
        console.log("Unknown grade");
}